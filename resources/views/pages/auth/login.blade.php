    <script>
        $(document).ready(function() {
            $('#login-form').submit(function(e) {
                e.preventDefault(); // Prevent the normal form submission

                $.ajax({
                    url: '/login',
                    type: 'POST',
                    data: $(this).serialize(),
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    success: function(response) {
                        console.log("AJAX success response:",
                            response); // Log response to console for debugging

                        if (response.success && response.message === "OTP sent successfully!") {
                            toastr.success(response
                                .message); // Display success toast notification
                            window.location.href = '/otp-verification'; // Redirect with email
                        } else {
                            toastr.error(response.message ||
                                'An unexpected error occurred. Please try again.'
                            ); // Show error if the expected message isn't received
                            $('#login-form')[0].reset(); // Optionally reset the form
                        }
                    },
                    error: function(xhr) {
                        console.log("AJAX error response:", xhr); // Log error to console
                        toastr.error('Error: ' + (xhr.responseJSON.message || xhr
                            .statusText)); // Display error toast notification
                        $('#login-form')[0].reset(); // Reset the form on error
                    }
                });
            });
        });
    </script>


